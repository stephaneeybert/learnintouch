[   4]An SMS message can be sent to all the mobile phone numbers of the web site, or to a list of numbers.\n\nIt can also be sent to a mobile phone number manually specified when sending the message.\n\nA list of numbers is a set of numbers, grouped in a list created by an administrator.\n\nThis offers the capability to send messages not just to all of the numbers of the web site, but to some targetted groups of numbers.\n\nExcept in the case when the mobile phone number is manually specified when sending the message, the users having chosen not to receive SMS messages will not receive any.
