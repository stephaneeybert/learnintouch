[   0]Largeur des images:
[   1]La largeur des images lorsqu'elles sont affichées dans la liste.
[   4]Largeur des images (téléphone):
[   5]La largeur des images lorsqu'elles sont affichées dans la liste sur un téléphone ou un appareil portable à petit écran.
[   2]Afficher tous les liens:
[   3]Par défaut, lorsqu'aucune catégorie n'a encore été sélectionnée par un visiteur du site web, seuls les liens de la première catégorie sont affichées.\n\nMais il est possible d'afficher tous les liens au lieu de seulement ceux de la première catégorie.
[   6]Cacher le sélecteur:
[   7]Par défaut, un sélecteur de catégorie est affiché en haut de la liste des liens.\n\nMais ce sélecteur peut être caché pour interdire la sélection d'une autre catégorie.
[   8]Largeur d'un cycle d'images (modèle):
[   9]La largeur des images lorsqu'elles sont affichées en cycle dans un élement de modèle de site web.\n\nPar exemple, des banières affichées sur le côté d'un modèle de site web.
[  10]Largeur d'un cycle d'images (page):
[  11]La largeur des images lorsqu'elles sont affichées en cycle dans une page du site web.\n\nPar exemple, des banières affichées dans une page.
[  12]Vitesse du cycle d'images:
[  13]La vitesse à laquelle les images du cycle sont affichées est exprimée en secondes.
