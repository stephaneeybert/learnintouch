[   1]La largeur des images lorsqu'elles sont affichées dans la liste.
[   2]Largeur d'un cycle d'images (modèle):
[   3]La largeur des images lorsqu'elles sont affichées en cycle dans un élement de modèle de site web.\n\nPar exemple, des banières affichées sur le côté d'un modèle de site web.
[   4]Largeur des images (téléphone):
[   5]La largeur des images lorsqu'elles sont affichées dans la liste sur un téléphone ou un appareil portable à petit écran.
[   6]Largeur d'un cycle d'images (page):
[   7]La largeur des images lorsqu'elles sont affichées en cycle dans une page du site web.\n\nPar exemple, des banières affichées dans une page.
[  12]Vitesse du cycle d'images:
[  13]La vitesse à laquelle les images du cycle sont affichées est exprimée en secondes.

