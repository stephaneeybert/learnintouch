[   9]Plusieurs journaux peuvent être publiés simultanément sur le site web.\n\nUn journal peut être publié ou archivé.\n\nSi un journal est archivé alors il ne sera pas affiché dans le site web.\n\nIl ne faut pas confondre cette fonctionalité de publication/archivage avec les dates de publication/archivage.\n\nVous pouvez manuellement publier ou archiver un journal, mail il peut aussi être automatiquement publié ou archivé en fonction de ses dates de publication et d'archivage.\n\nLes articles d'un journal sont utilisés par la source RSS.\n\nRSS signifie Rich Site Summary et c'est la liste des articles affichés au format RSS.\n\nLa source RSS est un moyen standard et commun de partager les articles en les offrant à d'autres sites web ou programmes supportant le format RSS.\n\nL'affichage de la source RSS est controllé par une option qui est spécifiée dans les préférences.
