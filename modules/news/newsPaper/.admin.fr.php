[  71]Il peut devenir fastidieux d'avoir à naviguer dans la liste des journaux pour retrouver un journal particulier.\n\nPour éviter cela, il est possible de saisir tout ou partie du titre, de l'en-tête ou du pied de page d'un journal et de faire une recherche basée sur le texte saisi.\n\nLe résultat de la recherche affichera tous les jounaux correspondant au texte recherché.
