[  71]Il peut devenir fastidieux d'avoir à naviguer dans la liste des articles pour retrouver un article particulier.\n\nPour éviter cela, il est possible de saisir tout ou partie du titre, de l'extrait ou de la date de publication d'un article et de faire une recherche basée sur le texte saisi.\n\nLe résultat de la recherche affichera tous les articles correspondant au texte recherché.
