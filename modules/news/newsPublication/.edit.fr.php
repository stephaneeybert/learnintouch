[  15]Avec archives: 
[  16]Par défaut, le journal n'offre pas d'accès à ses articles archivés.\n\nMais il est possible d'afficher un lien en bas du journal qui offre un accès aux archives du journal.
[  17]Alignement des images du journal:
[  18]Lors de l'affichage d'un journal, par défaut la première image de chaque article n'est pas affichée.\n\nMais il est possible de l'afficher au dessus ou dans un coin du titre ou de l'extrait.
[  19]Gauche dans le coin de l'extrait
[  20]Droite dans le coin de l'extrait
[  21]Les articles d'un journal peuvent être affichés dans une ou plusieurs colonnes
[  22]Au dessus de l'extrait
[  23]Avec les autres journaux:
[  24]Par défaut, le journal n'offre pas d'accès aux autres journaux.\n\nMais il est possible d'afficher en bas du journal, une liste de noms de journaux qui offre accès aux autres journaux.
[  25]Avec les articles d'une rubrique:
[  26]Lors de l'affichage d'un journal, il est possible d'offrir pour chaque article, une page affichant les articles de la même rubrique.\n\nCette page affiche tous les articles de la rubrique.
[  27]Cacher les rubriques:
[  28]Par défaut, les rubriques des articles sont affichées dans les journaux.\n\nMais ces rubriques peuvent être cachées.\n\nVeuillez noter que si les articles d'un journal sont affichés sur plus d'une colonne alors les rubriques ne sont pas affichées.
[  29]Glisser en bas:
[  30]Par défaut, un clic sur un extrait d'article de journal, affiche l'article dans une page indépendante.\n\nMais il est possible d'afficher l'article au sein du journal, en le faisant glisser vers le bas, en dessous de l'extrait de l'article.
