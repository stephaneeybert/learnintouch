[   3]Auto archivage (jours):
[   4]Le nom est requis.
[   5]Les journaux d'une publication peuvent être automatiquement archivés après une certaine période.\n\nPour chaque journal de la publication qui n'a pas de date d'archivage, si le journal a une date de publication plus ancienne que la période, alors la date d'archivage du journal sera mise à la date du jour.\n\nLa période est exprimée en jours.
[   6]Auto suppression (jours):
[   7]Les journaux d'une publication peuvent être automatiquement supprimés après une certaine période.\n\nChaque journal de la publication qui a une date d'archivage plus ancienne que la période est effacé.\n\nLa période est exprimée en jours.
[   8]La période après laquelle les journaux sont effacés doit être plus grande que celle après laquelle les journaux sont archivés.\n\nLes journaux ne devraient pas être effacés à moins qu'il ne soient auparavant archivés.
