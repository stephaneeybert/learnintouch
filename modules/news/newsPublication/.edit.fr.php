[  12]Sécuriser l'accès:
[  13]Par défaut, les utilisateurs ne sont pas tenus de se connecter, pour lire les journaux d'une publication.\n\nMais les journaux peuvent être protégés et leur accès réservé aux utilisateurs enregistrés dans le site web.\n\nDans ce cas, un utilisateur devra se connecter avant de pouvoir lire ces journaux.
