[  71]Il peut devenir fastidieux d'avoir à naviguer dans la liste de l'historique des mailings pour retrouver un mailing particulier.\n\nPour éviter cela, il est possible de saisir tout ou partie du sujet, du contenu ou de la description d'un mailing et de faire une recherche basée sur le texte saisi.\n\nLe résultat de la recherche affichera tous les mailings correspondant au texte recherché.
