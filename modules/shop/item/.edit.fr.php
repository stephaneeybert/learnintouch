[  43]Date de publication:
[  44]Un article peut être créé et publié immédiatement, ou il peut être créé maintenant et publié plus tard.\n\nLa date de publication est la date à partir de laquelle l'article est publié.\n\nQuand l'article est publié, il est visible sur le site web.\n\nL'article ne sera pas affiché sur le site web avant cette date.\n\nPour retarder la publication d'un article, veuillez spécifier une date de publication dans le futur.\n\nPar défaut, la date de publication est la date du jour de création de l'article.
