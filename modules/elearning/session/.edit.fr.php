[  16]Description:
[  17]Une session est simplement une période de temps.\n\nMais elle peut avoir un nom.\n\nIl est plus facile de se référer à une session par un nom que par des dates.\n\nPar exemple, les enseignants et les participants peuvent parler de leur session de Septembre.
