[   5]Cours:
[   6]Une session doit avoir au moins un cours, mais elle peut en avoir plusieurs.\n\nSi une session a plusieurs cours alors tous les cours de la session commencent et se terminent en même temps.\n\nPour ajouter plus de cours à la session, veuillez aller à la liste des cours d'une session.
[   9]Une session peut voir une classe, mais pas nécessairement, ou elle peut en avoir plusieurs.\n\nAssigner une classe à une session n'a de sens que si la classe n'existera que pour la durée de la session.\n\nSi une session a plusieurs classes alors toutes les classes de la session commencent et se terminent en même temps.\n\nPour ajouter plus de classes à la session, veuillez aller à la liste des classes d'une session.
[  11]Il doit d'abord y avoir des cours pour pouvoir créer une session.
