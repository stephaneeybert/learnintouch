[  29]Afin de suivre un cours, un participant doit d'abord être enregistré à la session d'un cours.\n\nUne fois qu'il a été enregistré, il est alors capable de faire les exercices du cours.\n\nSi un enseignant est assigné au participant pour son cours, alors l'enseignant pourra suivre les résultats de son participant.\n\nUn participant peut aussi consulter la liste des exercices des cours auxquels il est inscrit.\n\nSi un participant est inscrit à un cours alors il peut accéder à la liste des exercices du cours en se connectant au site web.\n\nDans cette liste il peut voir les exercices qu'il a fait et ses résultats pour chaque exercice.\n\nIl peut aussi y voir les exercices qu'il n'a pas encore fait, ainsi que le prochain exercice à faire.\n\nVeuillez noter que les résultats d'un exercice ne sont enregistrés que lorsque l'exercice est fait pour la première fois.\n\nSi un exercice est refait alors les résultats ne sont pas enregistrés une seconde fois.\n\nCette liste peut donc donner au participant différents accès à ses exercices.\n\nIl peut ainsi soit faire toutes ses exercices, immédiatement et dans n'importe quel ordre, soit faire uniquement l'exercice spécifié par son enseignant.\n\nCette liste peut aussi lui permettre de refaire les exercices qu'il a déjà fait.
