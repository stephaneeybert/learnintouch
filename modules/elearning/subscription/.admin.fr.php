[  35]Il est possible de suivre la progression de l'exercice pendant que le participant répond aux questions.\n\nDans ce cas, les résultats des questions seront affichés au professeur pendant que le participant fait l'exercice.\n\nLe professeur pourra voir une progression graphique de chaque exercice.\n\nLes résultats en direct sont affichés seulement quand le participant est en ligne et en train de faire l'exercice.\n\nSi le participant est inactif alors un voyant se met à clignoter.\n\nSi le participant est absent alors rien n'est affiché.
