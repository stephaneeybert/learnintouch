[   7]Les menus de navigation offrent aux utilisateurs visitant le site web un moyen de naviguer au sein du site web.\n\nUne barre de navigation est composée d'une série d'éléments, chaque élément pointant vers une page web ou vers un site web.\n\nChaque élément est représenté par du texte ou par une image.\n\nUn élément qui est représenté par une image peut avoir une seconde image.\n\nCette seconde image s'affiche lorsque le curseur de la souris passe sur la première image de l'élément.
